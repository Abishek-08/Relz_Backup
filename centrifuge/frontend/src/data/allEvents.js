// Helper functions for data manipulation 
export const getEventById = (eventId) => { return allEvents.find(event => event.id === parseInt(eventId)); }; 

export const getSubEventById = (eventId, subEventId) => { const event = getEventById(eventId); return event?.subEventList?.find(subEvent => subEvent.id === parseInt(subEventId)); }; 

export const updateSubEventMedia = (eventId, subEventId, mediaData) => { const event = getEventById(eventId); if (event) { const subEvent = event.subEventList.find(sub => sub.id === parseInt(subEventId)); if (subEvent) { subEvent.uploadedMedia = mediaData; subEvent.mediaUploadedAt = new Date().toISOString(); if (mediaData.length > 0 && subEvent.status === 'active') { subEvent.status = 'pending'; } } } }; 

export const updateSubEventAnalytics = (eventId, subEventId, analyticsData) => { const event = getEventById(eventId); if (event) { const subEvent = event.subEventList.find(sub => sub.id === parseInt(subEventId)); if (subEvent) { subEvent.analyticsData = analyticsData; subEvent.processingStatus = 'completed'; subEvent.analysisCompletedAt = new Date().toISOString(); subEvent.status = 'completed'; } } }; 

